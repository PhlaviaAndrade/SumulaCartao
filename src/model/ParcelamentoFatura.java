public class ParcelamentoFatura {

   private String numOperacao;
   private String tipoOperacao;
   private String dataContratacao;
   private String qtdeParcela;
   private String valorParcela;
   private String valorTotal;
   private String canalContratacao;
    

    public String getNumOperacao() {
        return numOperacao;
    }

    public void setNumOperacao(String numOperacao) {
        this.numOperacao = numOperacao;
    }

    public String getTipoOperacao() {
        return tipoOperacao;
    }

    public void setTipoOperacao(String tipoOperacao) {
        this.tipoOperacao = tipoOperacao;
    }

    public String getDataContratacao() {
        return dataContratacao;
    }

    public void setDataContratacao(String dataContratacao) {
        this.dataContratacao = dataContratacao;
    }

    public String getQtdeParcela() {
        return qtdeParcela;
    }

    public void setQtdeParcela(String qtdeParcela) {
        this.qtdeParcela = qtdeParcela;
    }

    public String getValorParcela() {
        return valorParcela;
    }

    public void setValorParcela(String valorParcela) {
        this.valorParcela = valorParcela;
    }

    public String getValorTotal() {
        return valorTotal;
    }

    public void setValorTotal(String valorTotal) {
        this.valorTotal = valorTotal;
    }

    public String getCanalContratacao() {
        return canalContratacao;
    }

    public void setCanalContratacao(String canalContratacao) {
        this.canalContratacao = canalContratacao;
    }
 
    
    
    
                                    
    
    
    
    
    
    
    
}
