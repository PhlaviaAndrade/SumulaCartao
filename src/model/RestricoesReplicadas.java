public class RestricoesReplicadas {
    
    private String tipo;
    private String dtaOcorrencia;
    private String dtaBaixa;
    private boolean selected;

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getDtaOcorrencia() {
        return dtaOcorrencia;
    }

    public void setDtaOcorrencia(String dtaOcorrencia) {
        this.dtaOcorrencia = dtaOcorrencia;
    }

    public String getDtaBaixa() {
        return dtaBaixa;
    }

    public void setDtaBaixa(String dtaBaixa) {
        this.dtaBaixa = dtaBaixa;
    }

    public boolean isSelected() {
        return selected;
    }

    public void setSelected(boolean selected) {
        this.selected = selected;
    }
    
    
    
    
    
    
}
